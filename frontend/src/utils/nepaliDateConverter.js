// Precompute intervals to make lookup O(1)
const MONTH_INTERVALS = [];
let accumulatedDays = 0;

for (const [year, months, adYear, adMonth, adDay] of BS_CALENDAR_DATA) {
  let currentDate = new Date(adYear, adMonth - 1, adDay);

  for (let m = 0; m < 12; m++) {
    const days = months[m];
    const startDate = new Date(currentDate.getTime());
    const endDate = new Date(currentDate.getTime() + days * 86400000);

    MONTH_INTERVALS.push({
      bsYear: year,
      bsMonth: m + 1,
      days,
      startAdYear: startDate.getFullYear(),
      startAdMonth: startDate.getMonth() + 1,
      startAdDay: startDate.getDate(),
      startDate,
      endDate
    });

    currentDate = new Date(currentDate.getTime() + days * 86400000);
  }
}

/**
 * Convert AD to BS
 */
export function adToBs(adYear, adMonth, adDay) {
  // Normalize date at midnight local time to avoid DST shifts
  const adDate = new Date(adYear, adMonth - 1, adDay, 12, 0, 0);

  if (isNaN(adDate.getTime())) {
    throw new Error('Invalid AD date provided');
  }

  let matched = null;

  for (const interval of MONTH_INTERVALS) {
    if (adDate >= interval.startDate && adDate < interval.endDate) {
      matched = interval;
      break;
    }
  }

  if (!matched) {
    throw new Error('Date out of supported range (BS 1970–2100 / AD 1913–2043)');
  }

  // Calculate day difference
  const diffTime = adDate.getTime() - matched.startDate.getTime();
  const diffDays = Math.floor(diffTime / 86400000);
  const bsYear = matched.bsYear;
  const bsMonth = matched.bsMonth;
  const bsDay = diffDays + 1;

  const dayOfWeek = adDate.getDay();

  return {
    bs: {
      year: bsYear,
      month: bsMonth,
      day: bsDay,
      monthNameEn: BS_MONTH_NAMES_EN[bsMonth - 1],
      monthNameNe: BS_MONTH_NAMES_NE[bsMonth - 1],
      dayNameEn: DAY_NAMES_EN[dayOfWeek],
      dayNameNe: DAY_NAMES_NE[dayOfWeek],
      formatted: `${bsYear}-${String(bsMonth).padStart(2,'0')}-${String(bsDay).padStart(2,'0')}`,
      formattedFull: `${BS_MONTH_NAMES_EN[bsMonth-1]} ${bsDay}, ${bsYear} BS`,
      formattedNe: `${bsYear} ${BS_MONTH_NAMES_NE[bsMonth - 1]} ${bsDay}`
    },
    ad: {
      year: adYear,
      month: adMonth,
      day: adDay,
      monthNameEn: AD_MONTH_NAMES[adMonth - 1],
      dayNameEn: DAY_NAMES_EN[dayOfWeek],
      dayNameNe: DAY_NAMES_NE[dayOfWeek],
      formatted: `${adYear}-${String(adMonth).padStart(2,'0')}-${String(adDay).padStart(2,'0')}`,
      formattedFull: `${AD_MONTH_NAMES[adMonth - 1]} ${adDay}, ${adYear} AD`
    }
  };
}

/**
 * Convert BS to AD
 */
export function bsToAd(bsYear, bsMonth, bsDay) {
  const matched = MONTH_INTERVALS.find(r => r.bsYear === bsYear && r.bsMonth === bsMonth);

  if (!matched) {
    throw new Error(`BS year ${bsYear} month ${bsMonth} not found. Supported range: 1970–2100 BS`);
  }

  if (bsDay < 1 || bsDay > matched.days) {
    throw new Error(`Invalid day ${bsDay} for ${BS_MONTH_NAMES_EN[bsMonth-1]} ${bsYear} BS. Valid range: 1–${matched.days}`);
  }

  const adDate = new Date(matched.startDate.getTime() + (bsDay - 1) * 86400000);

  const adYear = adDate.getFullYear();
  const adMonth = adDate.getMonth() + 1;
  const adDay = adDate.getDate();
  const dayOfWeek = adDate.getDay();

  return {
    ad: {
      year: adYear,
      month: adMonth,
      day: adDay,
      monthNameEn: AD_MONTH_NAMES[adMonth - 1],
      dayNameEn: DAY_NAMES_EN[dayOfWeek],
      dayNameNe: DAY_NAMES_NE[dayOfWeek],
      formatted: `${adYear}-${String(adMonth).padStart(2,'0')}-${String(adDay).padStart(2,'0')}`,
      formattedFull: `${AD_MONTH_NAMES[adMonth - 1]} ${adDay}, ${adYear} AD`
    },
    bs: {
      year: bsYear,
      month: bsMonth,
      day: bsDay,
      monthNameEn: BS_MONTH_NAMES_EN[bsMonth - 1],
      monthNameNe: BS_MONTH_NAMES_NE[bsMonth - 1],
      dayNameEn: DAY_NAMES_EN[dayOfWeek],
      dayNameNe: DAY_NAMES_NE[dayOfWeek],
      formatted: `${bsYear}-${String(bsMonth).padStart(2,'0')}-${String(bsDay).padStart(2,'0')}`,
      formattedFull: `${BS_MONTH_NAMES_EN[bsMonth-1]} ${bsDay}, ${bsYear} BS`,
      formattedNe: `${bsYear} ${BS_MONTH_NAMES_NE[bsMonth - 1]} ${bsDay}`
    }
  };
}

/**
 * Get Today in both formats
 */
export function getToday() {
  const today = new Date();
  return adToBs(today.getFullYear(), today.getMonth() + 1, today.getDate());
}

/**
 * Get BS Month calendar data
 */
export function getBsMonthCalendar(bsYear, bsMonth) {
  const matched = MONTH_INTERVALS.find(r => r.bsYear === bsYear && r.bsMonth === bsMonth);

  if (!matched) {
    throw new Error(`Calendar data not found for BS ${bsYear}-${bsMonth}`);
  }

  const startDayOfWeek = matched.startDate.getDay();

  const days = [];
  for (let d = 1; d <= matched.days; d++) {
    const adDate = new Date(matched.startDate.getTime() + (d - 1) * 86400000);
    days.push({
      bsDay: d,
      adYear: adDate.getFullYear(),
      adMonth: adDate.getMonth() + 1,
      adDay: adDate.getDate(),
      dayOfWeek: adDate.getDay(),
      isWeekend: adDate.getDay() === 6
    });
  }

  return {
    bsYear,
    bsMonth,
    monthNameEn: BS_MONTH_NAMES_EN[bsMonth - 1],
    monthNameNe: BS_MONTH_NAMES_NE[bsMonth - 1],
    totalDays: matched.days,
    startDayOfWeek,
    days
  };
}
