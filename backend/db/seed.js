async function seedDatabase() {
  const db = await getDB();

  // Check existing data
  const existing = queryOne('SELECT COUNT(*) as cnt FROM bs_calendar');
  if (existing && existing.cnt > 0) {
    console.log(`✅ Database already has ${existing.cnt} records. Skipping seed.`);
    return;
  }

  console.log('🌱 Seeding BS calendar data (1970–2100 BS)...');

  let totalInserted = 0;

  for (const [year, months, adYear, adMonth, adDay] of BS_CALENDAR_DATA) {
    let currentDate = new Date(adYear, adMonth - 1, adDay);

    for (let m = 0; m < 12; m++) {
      run(
        `INSERT OR IGNORE INTO bs_calendar (bs_year, bs_month, days, start_ad_year, start_ad_month, start_ad_day)
         VALUES (?, ?, ?, ?, ?, ?)`,
        [year, m + 1, months[m], currentDate.getFullYear(), currentDate.getMonth() + 1, currentDate.getDate()]
      );
      totalInserted++;
      currentDate = new Date(currentDate.getTime() + months[m] * 24 * 60 * 60 * 1000);
    }
  }

  saveDB();
  console.log(`✅ Seeded ${totalInserted} BS month records (${BS_CALENDAR_DATA.length} years: 1970–2100 BS)`);
}

